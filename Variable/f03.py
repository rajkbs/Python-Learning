#String Literals

name = "Raj Kumar"
Name = 'Raj Kumar'
namE = '''Raj Kumar'''

#'''  ''' can be used for multi line string literals

print("with double quote :",name)
print("with single quote :",namE)
print("with three quote :",Name)

#Numeric Literals

mobileNo = 90909090909
salary = 89000.75

print("Mobile No :", mobileNo)
print("Salary :", salary)

#Special Literals

_name = None
print(_name)